# Initial values
a = 1
b = 2
c = 3
d = 4
e = 5

# Print initial values
print("Before swapping:")
print(f"a = {a}, b = {b}, c = {c}, d = {d}, e = {e}")

# Swapping values
a, b, c, d, e = e, d, c, b, a

# Print swapped values
print("After swapping:")
print(f"a = {a}, b = {b}, c = {c}, d = {d}, e = {e}")
